# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.

ganhahora = float(input("Informe quanto você ganha por hora \r\n R:"))
horatrabalho = float(input("Informe o quanto de horas você trabalhou no mês \r\n R:"))
print("O total do seu salário do mês referido é:",ganhahora*horatrabalho,"reais")


