# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
print("Escreva as 4 notas bimestrais e será calculada a média.")
num1 = int(input("Digite o primeira nota"))
num2 = int(input("Digite o segunda nota"))
num3 = int(input("Digite o terceira nota"))
num4 = int(input("Digite o quarta nota"))
print("A média das notas foram:",(num1+num2+num3+num4)/4)