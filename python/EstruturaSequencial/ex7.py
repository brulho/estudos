# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

lado = float(input("Informe o lado de um quadrado para ser calculado a área e também será mostrado o dobro desta área:"))
area = lado*lado
print("A área do lado informado é:", area)
print("O dobro dá área é:", area*2)